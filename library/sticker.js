exports.toBuffer = async PATH => {
  if (!PATH) return {
    buffer: Buffer.alloc(0),
    ext: "png",
    mimetype: "image/png"
  };
  const buffer = Buffer.isBuffer(PATH) ? PATH : /^data:.*?\/.*?;base64,/i.test(PATH) ? Buffer.from(PATH.split(`,`)[1], "base64") : /^https?:\/\//.test(PATH) ? await Func.fetchBuffer(PATH) : fs.existsSync(PATH) ? fs.readFileSync(PATH) : Buffer.alloc(0);
  if (!Buffer.isBuffer(buffer)) throw new Error("Result is not a buffer");
  const file = await fromBuffer(buffer);
  const fixbuffer = buffer instanceof ArrayBuffer ? Buffer.from(buffer) : buffer;
  if (!file) throw new Error("Invalid file type");
  return {
    buffer: fixbuffer,
    mime: file.mime,
    ext: file.ext
  };
};
exports.setExif = async (webpSticker, packname, author, extra = {}) => {
  return new Promise(async (resolve, reject) => {
    try {
      const img = new Image();
      const stickerPackId = Func.randomBytes(5).toString("hex");
      const json = {
        "sticker-pack-id": `${"yoshida-" + stickerPackId}`,
        "sticker-pack-name": packname,
        "sticker-pack-publisher": author,
        "sticker-pack-version": `${require("../package.json").version}`,
        "android-app-store-link": "https://play.google.com/store/apps/details?id=com.whatsapp",
        "ios-app-store-link": "https://apps.apple.com/us/app/whatsapp-messenger/",
        "bot-official-site": "https://yoshida.my.id",
        "sticker-pack-description": "This sticker is a sticker that has been generated by YOSHIDA.BOT",
        emojis: ["🤩", "🌟", "🥶", "💀", "😋", "😂", "😜", "❤️"],
        ...extra
      };
      let exifAttr = Buffer.from([73, 73, 42, 0, 8, 0, 0, 0, 1, 0, 65, 87, 7, 0, 0, 0, 0, 0, 22, 0, 0, 0]);
      let jsonBuffer = Buffer.from(JSON.stringify(json), "utf8");
      let exif = Buffer.concat([exifAttr, jsonBuffer]);
      exif.writeUIntLE(jsonBuffer.length, 14, 4);
      await img.load(webpSticker);
      img.exif = exif;
      resolve(await img.save(null));
    } catch (e) {
      reject(e);
    }
  });
};
exports.makeSticker = async (file, data = {
  pack: "",
  author: "",
  keepScale: true,
  removebg: "HQ",
  circle: false
}) => {
  return new Promise(async (resolve, reject) => {
    try {
      const buffer = await this.toBuffer(file);
      if (buffer.mime === "image/webp") {
        resolve(await this.setExif(buffer.buffer, data.pack, data.author));
        return;
      }
      const config2 = {
        ...data,
        processOptions: {
          crop: !data.keepScale,
          fps: 60,
          startTime: "00:00:00.0",
          endTime: "00:00:7.0",
          loop: 0
        }
      };
      const DEFAULT_URL = "https://sticker-api.openwa.dev/";
      const Type = buffer.mime.includes("image") ? "image" : "file";
      const url = `${DEFAULT_URL}${Type === "image" ? "prepareWebp" : "convertMp4BufferToWebpDataUrl"}`;
      const {
        data: responseData
      } = await axios.post(url, JSON.stringify({
        ...setting,
        stickerMetadata: config2,
        [Type]: `data:${buffer.mime};base64,${buffer.buffer.toString("base64")}`
      }), {
        headers: {
          Accept: "application/json, text/plain, */*",
          "Content-Type": "application/json;charset=utf-8",
          "User-Agent": "WhatsApp/2.2037.6 Mozilla/5.0 (Macintosh; Intel Mac OS X 10_15_6) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/85.0.4183.83 Safari/537.36"
        },
        maxBodyLength: Infinity,
        maxContentLength: Infinity
      });
      if (Type === "image") {
        resolve(await this.setExif(Buffer.from(responseData.webpBase64, "base64"), data.pack, data.author));
      } else {
        const webpBase = responseData.replace(/^data:(.*?);base64,/, "").replace(/ /g, "+");
        const fileBuffer = Buffer.from(webpBase, "base64");
        resolve(await this.setExif(fileBuffer, data.pack, data.author));
      }
    } catch (error) {
      reject(error);
    }
  });
};